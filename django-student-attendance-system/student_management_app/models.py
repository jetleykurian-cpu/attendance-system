class StudentResult(models.Model):
    id = models.AutoField(primary_key=True)
    student_id = models.ForeignKey(Students, on_delete=models.CASCADE)
    subject_id = models.ForeignKey(Subjects, on_delete=models.CASCADE)
    exam_marks = models.IntegerField(default=0)
    assignment_marks = models.IntegerField(default=0)
    assignment_out_of = models.IntegerField(default=50)
    exam_out_of = models.IntegerField(default=50)
    remarks = models.TextField(null=True, blank=True)
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now_add=True)

    def total_marks(self):
        return self.exam_marks + self.assignment_marks

    def total_out_of(self):
        return self.exam_out_of + self.assignment_out_of

    def grade(self):
        total = self.total_marks()
        if total >= 90:
            return "A+"
        elif total >= 80:
            return "A"
        elif total >= 70:
            return "B"
        elif total >= 60:
            return "C"
        else:
            return "F"

    def __str__(self):
        return f"{self.student_id.admin.username} - {self.subject_id.subject_name}"


